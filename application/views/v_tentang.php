<script>
    var sidebar = {};
        sidebar.element = $('nav.sidebar');
        sidebar.baseMargin = 0;
        sidebar.adjust = function(){
            sidebar.element.css('top',sidebar.baseMargin+window.pageYOffset);
        }
        sidebar.activate = function(element){
            var ul = $(element).parents('ul');
            var li = $(element).parents('li');
            ul.find('li').removeClass('active');
            li.addClass('active');
        }
    $(sidebar.element).find('li a').click(function(e){
        sidebar.activate(this);
    })
    $(window).scroll(function(){
        sidebar.adjust();
    })
</script>

